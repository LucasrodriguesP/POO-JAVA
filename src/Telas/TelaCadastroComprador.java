    private void jTCompradorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTCompradorMouseClicked
        // TODO add your handling code here:
        if (jTComprador.getSelectedRow() != -1) {
            txtRegiao.setText(jTComprador.getValueAt(jTComprador.getSelectedRow(), 0).toString());
            txtValor.setText(jTComprador.getValueAt(jTComprador.getSelectedRow(), 1).toString());
            txtNome.setText(jTComprador.getValueAt(jTComprador.getSelectedRow(), 2).toString());
            ftxtCPF.setText(jTComprador.getValueAt(jTComprador.getSelectedRow(), 3).toString());
            ftxtTelefone.setText(jTComprador.getValueAt(jTComprador.getSelectedRow(), 4).toString());
        }

    }//GEN-LAST:event_jTCompradorMouseClicked

