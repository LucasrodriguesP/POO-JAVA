    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if (jTComprador.getSelectedRow() != -1) {
            DefaultTableModel dtmComprador = (DefaultTableModel) jTComprador.getModel();
            dtmComprador.removeRow(jTComprador.getSelectedRow());
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha!");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dtmComprador = (DefaultTableModel) jTComprador.getModel();
        dtmComprador.setRowCount(0);
        CompradorDAO cdao = new CompradorDAO();

        for (Comprador c : cdao.buscar()) {

            Object[] dados = {c.getRegiao(), c.getValor(), c.getNome(), c.getCpf(), c.getTelefone()};
            dtmComprador.addRow(dados);

        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:        
        if (jTComprador.getSelectedRow() != -1) {
            jTComprador.setValueAt(txtRegiao.getText(), jTComprador.getSelectedRow(), 0);
            jTComprador.setValueAt(txtValor.getText(), jTComprador.getSelectedRow(), 1);
            jTComprador.setValueAt(txtNome.getText(), jTComprador.getSelectedRow(), 2);
            jTComprador.setValueAt(ftxtCPF.getText(), jTComprador.getSelectedRow(), 3);
            jTComprador.setValueAt(ftxtTelefone.getText(), jTComprador.getSelectedRow(), 4);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

